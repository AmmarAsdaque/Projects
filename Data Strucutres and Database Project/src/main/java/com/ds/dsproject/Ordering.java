    private void jLabel6MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel6MousePressed
        if(cart.head == null){
            jOptionPane1.showMessageDialog(this, "Oops, You haven't added any "
                + "item to cart. Please add item to the cart");
            return;
        }
        cart.commit();
        totalPriceField.setText("");
        cart.deleteAtHead();
        reciptArea.setText(cart.display().toString());
    }//GEN-LAST:event_jLabel6MousePressed
    
    public void updateTextField(int row){
        if(jTable1.getRowCount() == 0){
            return;
        }
        String x = (String) jTable1.getValueAt(row, 1);
        if(x != null){
            item = (int) jTable1.getValueAt(row,0);
            price = (float) jTable1.getValueAt(row, 2);
            itemIDToOrderField.setText(x);
        }else
            itemIDToOrderField.setText("Select Valid Item");
    }
